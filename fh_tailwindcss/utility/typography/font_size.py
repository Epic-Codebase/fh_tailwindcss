from ...tailwind import TailwindEnum

class FontSize(TailwindEnum):
    """
    Enum class representing different text size values in Tailwind CSS.

    Attributes:
        XSMALL (str): Extra small text size (0.75rem).
        SMALL (str): Small text size (0.875rem).
        BASE (str): Default base text size (1rem).
        LARGE (str): Large text size (1.125rem).
        XLARGE (str): Extra large text size (1.25rem).
        XLARGE2 (str): 2x extra large text size (1.5rem).
        XLARGE3 (str): 3x extra large text size (1.875rem).
        XLARGE4 (str): 4x extra large text size (2.25rem).
        XLARGE5 (str): 5x extra large text size (3rem).
        XLARGE6 (str): 6x extra large text size (3.75rem).
        XLARGE7 (str): 7x extra large text size (4.5rem).
        XLARGE8 (str): 8x extra large text size (6rem).
        XLARGE9 (str): 9x extra large text size (8rem).
    """
    XSMALL = 'text-xs'
    SMALL = 'text-sm'
    BASE = 'text-base'
    LARGE = 'text-lg'
    XLARGE = 'text-xl'
    XLARGE2 = 'text-2xl' 
    XLARGE3 = 'text-3xl'
    XLARGE4 = 'text-4xl'
    XLARGE5 = 'text-5xl'
    XLARGE6 = 'text-6xl'
    XLARGE7 = 'text-7xl'
    XLARGE8 = 'text-8xl'
    XLARGE9 = 'text-9xl'
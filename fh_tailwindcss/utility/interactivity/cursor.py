from ...tailwind import TailwindEnum

# https://tailwindcss.com/docs/cursor

class Cursor(TailwindEnum):
    """
    Utilities for controlling the cursor style when hovering over an element.

    Attributes:
        AUTO (str): Sets the cursor style to "auto".
        DEFAULT (str): Sets the cursor style to "default".
        POINTER (str): Sets the cursor style to "pointer".
        WAIT (str): Sets the cursor style to "wait".
        TEXT (str): Sets the cursor style to "text".
        MOVE (str): Sets the cursor style to "move".
        HELP (str): Sets the cursor style to "help".
        NOT_ALLOWED (str): Sets the cursor style to "not-allowed".
        NONE (str): Hides the cursor.
        CONTEXT_MENU (str): Sets the cursor style to "context-menu".
        PROGRESS (str): Sets the cursor style to "progress".
        CELL (str): Sets the cursor style to "cell".
        CROSSHAIR (str): Sets the cursor style to "crosshair".
        VERTICAL_TEXT (str): Sets the cursor style to "vertical-text".
        ALIAS (str): Sets the cursor style to "alias".
        COPY (str): Sets the cursor style to "copy".
        NO_DROP (str): Sets the cursor style to "no-drop".
        GRAB (str): Sets the cursor style to "grab".
        GRABBING (str): Sets the cursor style to "grabbing".
        ALL_SCROLL (str): Sets the cursor style to "all-scroll".
        COL_RESIZE (str): Sets the cursor style to "col-resize".
        ROW_RESIZE (str): Sets the cursor style to "row-resize".
        N_RESIZE (str): Sets the cursor style to "n-resize".
        E_RESIZE (str): Sets the cursor style to "e-resize".
        S_RESIZE (str): Sets the cursor style to "s-resize".
        W_RESIZE (str): Sets the cursor style to "w-resize".
        NE_RESIZE (str): Sets the cursor style to "ne-resize".
        NW_RESIZE (str): Sets the cursor style to "nw-resize".
        SE_RESIZE (str): Sets the cursor style to "se-resize".
        SW_RESIZE (str): Sets the cursor style to "sw-resize".
        EW_RESIZE (str): Sets the cursor style to "ew-resize".
        NS_RESIZE (str): Sets the cursor style to "ns-resize".
        NESW_RESIZE (str): Sets the cursor style to "nesw-resize".
        NWSE_RESIZE (str): Sets the cursor style to "nwse-resize".
        ZOOM_IN (str): Sets the cursor style to "zoom-in".
        ZOOM_OUT (str): Sets the cursor style to "zoom-out".
    """
    AUTO = "cursor-auto"
    DEFAULT = "cursor-default"
    POINTER = "cursor-pointer"
    WAIT = "cursor-wait"
    TEXT = "cursor-text"
    MOVE = "cursor-move"
    HELP = "cursor-help"
    NOT_ALLOWED = "cursor-not-allowed"
    NONE = "cursor-none"
    CONTEXT_MENU = "cursor-context-menu"
    PROGRESS = "cursor-progress"
    CELL = "cursor-cell"
    CROSSHAIR = "cursor-crosshair"
    VERTICAL_TEXT = "cursor-vertical-text"
    ALIAS = "cursor-alias"
    COPY = "cursor-copy"
    NO_DROP = "cursor-no-drop"
    GRAB = "cursor-grab"
    GRABBING = "cursor-grabbing"
    ALL_SCROLL = "cursor-all-scroll"
    COL_RESIZE = "cursor-col-resize"
    ROW_RESIZE = "cursor-row-resize"
    N_RESIZE = "cursor-n-resize"
    E_RESIZE = "cursor-e-resize"
    S_RESIZE = "cursor-s-resize"
    W_RESIZE = "cursor-w-resize"
    NE_RESIZE = "cursor-ne-resize"
    NW_RESIZE = "cursor-nw-resize"
    SE_RESIZE = "cursor-se-resize"
    SW_RESIZE = "cursor-sw-resize"
    EW_RESIZE = "cursor-ew-resize"
    NS_RESIZE = "cursor-ns-resize"
    NESW_RESIZE = "cursor-nesw-resize"
    NWSE_RESIZE = "cursor-nwse-resize"
    ZOOM_IN = "cursor-zoom-in"
    ZOOM_OUT = "cursor-zoom-out"
